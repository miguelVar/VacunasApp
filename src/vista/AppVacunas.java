    private VacunasController vacunasController = new VacunasController();
    
    private void cargarTabla(){
        DefaultTableModel defaultTableModel = new DefaultTableModel();
        VacunasTable.setModel(defaultTableModel);
        defaultTableModel.addColumn("ID VACUNA");
        defaultTableModel.addColumn("NOMBRE VACUNA");
        defaultTableModel.addColumn("DESCRIPCIÓN VACUNA");
        
        List<Vacuna> vacunas = vacunasController.obtenerVacunas();
        Iterator<Vacuna> iterator = vacunas.iterator();
        while (iterator.hasNext()) {            
            Vacuna vacuna = iterator.next();
            defaultTableModel.addRow(new Object[]{
            vacuna.getId(),
            vacuna.getNombre(),
            vacuna.getDescripcion()});
        }
    }
    
    public void cargarVacunas(){
        List<Vacuna> vacunas = vacunasController.obtenerVacunas();
        Iterator<Vacuna> iterator = vacunas.iterator();
        while (iterator.hasNext()) {
            Vacuna vacuna = iterator.next();
            jComboBox1.addItem(vacuna.getNombre()+","+vacuna.getId());
        }
    }
