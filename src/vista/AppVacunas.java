    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        Integer idVacuna = Integer.parseInt(jTextField6.getText());

        DefaultTableModel defaultTableModelDosis = new DefaultTableModel();
        dosisTable.setModel(defaultTableModelDosis);
        defaultTableModelDosis.addColumn("ID DOSIS");
        defaultTableModelDosis.addColumn("VALOR TIEMPO");
        defaultTableModelDosis.addColumn("UNIDAD TIEMPO");
        defaultTableModelDosis.addColumn("ID VACUNA");

        java.util.Set<Dosis> dosis = vacunasController.obtenerDosisVacuna(idVacuna);
        Iterator<Dosis> iterator = dosis.iterator();
        while (iterator.hasNext()) {
            Dosis dosis1 = iterator.next();
            defaultTableModelDosis.addRow(new Object[]{
                dosis1.getId(),
                dosis1.getValorTiempo(),
                dosis1.getUnidadTimepo(),
                dosis1.getVacuna().getId()});
        }

    }//GEN-LAST:event_jButton5ActionPerformed

